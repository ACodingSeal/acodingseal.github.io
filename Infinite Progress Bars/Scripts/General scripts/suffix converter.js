function lowToExponential(e) {
if (e.greaterThanOrEqualTo(1e3) && e.lessThan(1e21)) {
	result = Number(e).toExponential(3).replace(/[+]/g, '');
} else {
	result = e.toStringWithDecimalPlaces(3);
}
return result;
}
function notateInt(e) {
	var extraZeroes;
	if (e.greaterThanOrEqualTo(1e3) && e.lessThan(1e6)) {
		result = Number(e).toLocaleString();
	} else if (e.greaterThanOrEqualTo(1e6) && Math.floor(e.exponent / 3) < (suffixes10k.length) && settings.suffixes === true) {
		switch(((e.exponent / 3) - Math.floor(e.exponent / 3)).toFixed(1)) {
			case '0.0':
				extraZeroes = 0;
			break;
			case '0.3':
				extraZeroes = 1;
			break;
			case '0.7':
				extraZeroes = 2;
		}
		result = (e.mantissa * (10 ** extraZeroes)).toFixed(3) + '' + suffixes10k[Math.floor(e.exponent / 3)];
	} else {
		result = lowToExponential(e);
	}
	return result;
}